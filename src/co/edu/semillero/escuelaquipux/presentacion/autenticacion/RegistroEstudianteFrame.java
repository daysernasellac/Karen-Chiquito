    private boolean validacionEstudiante(EstudianteDTO estudiante){
        if(estudiante.getNombre().isEmpty()||estudiante.getApellido().isEmpty()||estudiante.getNumeroDocumento().isEmpty()){
            jLabel4.setText("Uno o varios de los datos obligatorios estan vacios");
            return false;
        }
        jLabel4.setText("");
        return true;
    }
