    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        EstudianteDTO estudiante = new EstudianteDTO();
        estudiante.setNombre(jTextField1.getText());
        estudiante.setApellido(jTextField2.getText());
        estudiante.setNumeroDocumento(jTextField3.getText());
        if(validacionEstudiante(estudiante)){
            EstudianteNegocio estudianteNegocio = new EstudianteNegocioImpl();
            estudianteNegocio.registrar(estudiante);
            MainFrame mainFrame = new MainFrame();
            mainFrame.setVisible(true);
            this.setVisible(false);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

