    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        LoginValidacion loginValidacion = new LoginValidacion();

        LoginUsuarioDTO loginUsuario = new LoginUsuarioDTO();
        loginUsuario.setUsuario(usuario.getText());
        loginUsuario.setPassword(clave.getText());

        if(loginValidacion.login(loginUsuario)){
            System.out.println("Usuario y clave correctos");
            MainFrame mainFrame = new MainFrame();
            mainFrame.setVisible(true);
            this.setVisible(false);
        }
        else{
            System.out.println("Usuario y clave incorrectos");
            this.jTextField1.setText("campos no validos");
        }
    }//GEN-LAST:event_loginActionPerformed

